export const FIAT_CURRENCY_CODES: ReadonlySet<string> = new Set(FIAT_CODES_ARRAY);

/** True if code is a 3-letter ISO-style fiat we support (excludes crypto / AGLD-style tickers). */
export function isFiatCurrencyCode(code: string | undefined | null): boolean {
  if (code == null) return false;
  const c = String(code).trim().toUpperCase();
  if (!/^[A-Z]{3}$/.test(c)) return false;
  return FIAT_CURRENCY_CODES.has(c);
}

/** Dedupe, keep only fiat, sort A–Z for stable pickers. */
export function filterFiatCurrencyCodes(codes: string[]): string[] {
  const out = new Set<string>();
  for (const raw of codes) {
    const c = String(raw).trim().toUpperCase();
    if (isFiatCurrencyCode(c)) out.add(c);
  }
  return Array.from(out).sort();
}

/** Keys from an API `conversion_rates` object, fiat-only. */
export function fiatKeysFromConversionRates(
  rates: Record<string, number> | undefined | null
): string[] {
  if (!rates || typeof rates !== "object") return [];
  return filterFiatCurrencyCodes(Object.keys(rates));
}
